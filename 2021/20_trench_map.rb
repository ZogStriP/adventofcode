algorithm, image = DATA.read.split("\n\n")

image = image.split("\n")

min = -1
ymax = image.size + 1
xmax = image[0].size + 1

pixels = Hash.new(0)

image.each_with_index { |l, y|
  l.each_char.with_index { |c, x|
    pixels[[y, x]] = c[?#] ? 1 : 0
  }
}

N = [-1,0,1].product([-1,0,1])

puts [2, 48].map {
  _1.times { |t|
    new = Hash.new(algorithm[0][?#] ? 1 - (t % 2) : 0)

    (min...ymax).each { |y|
      (min...xmax).each { |x|
        index = N.map { |dy, dx| pixels[[y + dy, x + dx]] }.join.to_i(2)
        new[[y, x]] = algorithm[index][?#] ? 1 : 0
      }
    }

    pixels = new

    min  -= 1
    xmax += 1
    ymax += 1
  }

  pixels.values.tally[1]
}

p min, xmax

