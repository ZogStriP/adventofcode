map = DATA.map { _1.chop.bytes }
Y = map.size
X = map[0].size
map.flatten!

goal = nil
start = nil
starts = []

Y.times { |y|
  X.times { |x|
    case map[c = y * X + x].chr
    when ?a; starts << c
    when ?S; starts << c; map[start = c] = ?a.ord
    when ?E; map[goal = c] = ?z.ord
    end
  }
}

steps = -> (starts) {
  q = starts.dup
  d = Array.new(map.size, map.size + 1)
  starts.each { d[_1] = 0 }

  while c = q.shift
    break if c == goal

    y, x = c.divmod(X)

    [
      (c - X if y > 0),
      (c - 1 if x > 0),
      (c + 1 if x + 1 < X),
      (c + X if y + 1 < Y),
    ].each { (d[_1] = d[c] + 1; q << _1) if _1 && map[_1] <= map[c] + 1 && d[_1] > d[c] + 1 }
  end

  d[goal]
}

p steps[[start]], steps[starts]

__END__
